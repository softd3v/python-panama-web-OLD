                                    <p>Guido van Rossum es un programador y matemático holandés, más conocido por ser el
                                        creador del lenguaje de programación Python. Nacido el 31 de enero de 1956 en
                                        los Países Bajos, van Rossum comenzó a programar a los 16 años mientras
                                        estudiaba matemáticas en la Universidad de Amsterdam.</p>
                                    <p>En 1982, van Rossum se unió al Instituto Nacional de Investigación en Matemáticas
                                        y Ciencias de la Computación (CWI, por sus siglas en inglés) en los Países
                                        Bajos. Fue allí donde comenzó a trabajar en el desarrollo del lenguaje de
                                        programación ABC, que se convirtió en la inspiración para la creación de Python.
                                    </p>
                                    <p>En diciembre de 1989, van Rossum comenzó a trabajar en Python como un proyecto
                                        personal en su tiempo libre mientras trabajaba en CWI. La idea detrás de Python
                                        era crear un lenguaje de programación que fuera fácil de aprender y de usar,
                                        pero que también fuera poderoso y flexible. El nombre "Python" fue elegido por
                                        van Rossum en honor al grupo de comediantes británicos Monty Python.</p>
                                    <p>La primera versión pública de Python, la versión 0.9.0, fue lanzada en febrero de
                                        1991. Desde entonces, Python ha crecido en popularidad y ha sido utilizado en
                                        una amplia variedad de aplicaciones, desde el desarrollo web y móvil hasta la
                                        inteligencia artificial y el aprendizaje automático.</p>
                                    <p>Van Rossum ha liderado el desarrollo de Python durante muchos años y ha
                                        desempeñado un papel importante en la comunidad de Python. En 2001, fue
                                        galardonado con el premio de "Pythonico del año" en la conferencia anual de
                                        Python. En 2003, se unió a Google, donde trabajó en el equipo de desarrollo de
                                        Google App Engine. En 2012, van Rossum se unió a Dropbox como un miembro
                                        distinguido del equipo de ingeniería.</p>
                                    <p>En 2018, van Rossum anunció que se retiraría de su papel de líder de desarrollo
                                        de Python. Desde entonces, ha seguido trabajando en el desarrollo de Python como
                                        miembro de la Python Software Foundation, la organización sin fines de lucro que
                                        supervisa el desarrollo de Python.</p>

